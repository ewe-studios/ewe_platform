use std::path::{Path, PathBuf};

use super::error::WasmBinError;
use super::{BinSection, GeneratedFile, WasmBinPlan, WasmEntrypoint, WasmOutput};

/// WHY: The plan needs to be built from entrypoints without side effects
/// so `list` can show what would happen without modifying anything.
///
/// WHAT: Builds a `WasmBinPlan` from discovered entrypoints.
///
/// HOW: For each entrypoint, computes the binary file path, content,
/// Cargo.toml `[[bin]]` section, and expected WASM output paths.
///
/// # Errors
///
/// Returns `WasmBinError::NoEntrypoints` if no entrypoints were found.
///
/// # Panics
///
/// Never panics.
pub fn build_plan(
    crate_name: &str,
    crate_dir: &Path,
    entrypoints: &[WasmEntrypoint],
) -> Result<WasmBinPlan, WasmBinError> {
    if entrypoints.is_empty() {
        return Err(WasmBinError::NoEntrypoints(crate_dir.to_path_buf()));
    }

    let mut bin_sections = Vec::with_capacity(entrypoints.len());
    let mut generated_files = Vec::with_capacity(entrypoints.len());
    let mut wasm_outputs = Vec::with_capacity(entrypoints.len());

    for ep in entrypoints {
        let bin_path = format!("bin/{}/main.rs", ep.name);

        bin_sections.push(BinSection {
            name: ep.name.clone(),
            path: bin_path.clone(),
        });

        let content = generate_main_rs(ep);
        generated_files.push(GeneratedFile {
            path: PathBuf::from(&bin_path),
            content,
        });

        wasm_outputs.push(WasmOutput {
            bin_name: ep.name.clone(),
            debug_path: format!("target/wasm32-unknown-unknown/debug/{}.wasm", ep.name),
            release_path: format!("target/wasm32-unknown-unknown/release/{}.wasm", ep.name),
        });
    }

    Ok(WasmBinPlan {
        crate_name: crate_name.to_string(),
        crate_dir: crate_dir.to_path_buf(),
        entrypoints: entrypoints.to_vec(),
        bin_sections,
        generated_files,
        wasm_outputs,
    })
}

/// WHY: Each WASM entrypoint needs a `main.rs` file that calls the
/// annotated function using its fully qualified path.
///
/// WHAT: Generates the content of a `bin/{name}/main.rs` file.
///
/// HOW: Constructs a `main()` function that calls the entrypoint function
/// via its qualified module path.
///
/// # Panics
///
/// Never panics.
fn generate_main_rs(ep: &WasmEntrypoint) -> String {
    format!(
        "// WASM Entrypoint: {name}\n\
         // Description: {desc}\n\
         //\n\
         // Generated by ewe_platform wasm_bins - DO NOT EDIT\n\
         // Source: {qualified_path}\n\
         \n\
         // Required for wasm32-unknown-unknown target\n\
         #[no_mangle]\n\
         pub extern \"C\" fn main() {{\n\
         {INDENT}{qualified_path}();\n\
         }}\n",
        name = ep.name,
        desc = ep.description,
        qualified_path = ep.qualified_path,
        INDENT = "    ",
    )
}
