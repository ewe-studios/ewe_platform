//! API catalog for code generation.
//!
//! WHY: Both client generation and provider wrapper generation need to know
//!      what APIs exist, their endpoints, and what functions to generate.
//!      This provides a single source of truth instead of regex parsing.
//!
//! WHAT: `ApiCatalog` struct with per-API endpoint listings and function names.
//!
//! HOW: Uses `foundation_openapi` spec processing to extract structured data
//!      about providers, APIs, endpoints, and their builder/task functions.

use crate::{process_spec, EndpointInfo, OperationType};
use std::path::Path;

/// Catalog of all APIs for a provider.
#[derive(Debug, Clone)]
pub struct ApiCatalog {
    /// Provider name (e.g., "gcp", "stripe")
    pub provider: String,
    /// List of APIs within this provider
    pub apis: Vec<ApiInfo>,
}

/// Information about a single API (e.g., "compute", "cloudkms" for GCP).
#[derive(Debug, Clone)]
pub struct ApiInfo {
    /// API name (e.g., "compute", "admin")
    pub name: String,
    /// Base URL for this API
    pub base_url: Option<String>,
    /// All endpoints in this API
    pub endpoints: Vec<EndpointInfo>,
}

impl ApiInfo {
    /// Get the builder function name for an endpoint.
    #[must_use]
    pub fn builder_fn_name(&self, endpoint: &EndpointInfo) -> String {
        to_snake_case(&endpoint.operation_id) + "_builder"
    }

    /// Get the task function name for an endpoint.
    #[must_use]
    pub fn task_fn_name(&self, endpoint: &EndpointInfo) -> String {
        to_snake_case(&endpoint.operation_id) + "_task"
    }

    /// Get the execute function name for an endpoint.
    #[must_use]
    pub fn execute_fn_name(&self, endpoint: &EndpointInfo) -> String {
        to_snake_case(&endpoint.operation_id) + "_execute"
    }

    /// Get the convenience function name for an endpoint.
    #[must_use]
    pub fn convenience_fn_name(&self, endpoint: &EndpointInfo) -> String {
        to_snake_case(&endpoint.operation_id)
    }

    /// Get the args struct name for an endpoint.
    ///
    /// Derives from the canonical snake_case function name to ensure
    /// consistency with the names generated by clients.rs.
    #[must_use]
    pub fn args_struct_name(&self, endpoint: &EndpointInfo) -> String {
        to_pascal_case(&self.convenience_fn_name(endpoint)) + "Args"
    }
}

/// Builder for creating an `ApiCatalog` from various sources.
pub struct ApiCatalogBuilder {
    provider: String,
}

impl ApiCatalogBuilder {
    #[must_use]
    pub fn new(provider: &str) -> Self {
        Self {
            provider: provider.to_string(),
        }
    }

    /// Build catalog from a single `OpenAPI` spec file.
    ///
    /// # Errors
    ///
    /// Returns an error string if the file cannot be read from `spec_path`
    /// or if its contents cannot be parsed as an `OpenAPI` spec.
    pub fn from_spec_file(&self, spec_path: &Path) -> Result<ApiCatalog, String> {
        let content =
            std::fs::read_to_string(spec_path).map_err(|e| format!("Failed to read spec: {e}"))?;

        self.from_spec_content(&content)
    }

    /// Build catalog from spec content string.
    ///
    /// # Errors
    ///
    /// Returns an error string if `content` cannot be parsed as an `OpenAPI`
    /// spec, including when wrapped in a known envelope object.
    pub fn from_spec_content(&self, content: &str) -> Result<ApiCatalog, String> {
        // Try direct processing first
        let processor = process_spec(content)
            .or_else(|_| {
                // Try unwrapping from nested structure (e.g., {"openapi.json": {...}})
                let wrapped: serde_json::Value =
                    serde_json::from_str(content).map_err(|e| format!("JSON parse error: {e}"))?;
                if let Some(obj) = wrapped.as_object() {
                    for key in ["openapi.json", "openapi", "spec"] {
                        if let Some(inner) = obj.get(key) {
                            if let Ok(proc) = process_spec(&inner.to_string()) {
                                return Ok(proc);
                            }
                        }
                    }
                }
                Err("Failed to parse spec".to_string())
            })
            .map_err(|e| format!("Failed to process spec: {e}"))?;

        let endpoints = processor.endpoints();
        let base_url = processor.base_url();

        let api = ApiInfo {
            name: self.provider.clone(),
            base_url,
            endpoints,
        };

        Ok(ApiCatalog {
            provider: self.provider.clone(),
            apis: vec![api],
        })
    }

    /// Build catalog from multiple `OpenAPI` spec files (for multi-API providers like GCP).
    ///
    /// # Errors
    ///
    /// Returns an error string if any of the provided spec contents fail
    /// to parse. The error includes the offending API name.
    pub fn from_spec_files(
        &self,
        specs: Vec<(String, String)>, // (api_name, spec_content)
    ) -> Result<ApiCatalog, String> {
        let mut apis = Vec::new();

        for (api_name, content) in specs {
            let processor = process_spec(&content)
                .map_err(|e| format!("Failed to process spec for {api_name}: {e}"))?;

            let endpoints = processor.endpoints();
            let base_url = processor.base_url();

            apis.push(ApiInfo {
                name: api_name,
                base_url,
                endpoints,
            });
        }

        // Sort APIs by name for consistent ordering
        apis.sort_by(|a, b| a.name.cmp(&b.name));

        Ok(ApiCatalog {
            provider: self.provider.clone(),
            apis,
        })
    }

    /// Build catalog from a directory of API specs (`provider/api_name/openapi.json`).
    /// Skips specs that fail to parse, logging a warning.
    ///
    /// # Errors
    ///
    /// Returns an error string if reading the directory or its single
    /// top-level `openapi.json` fails. Per-API parse failures are logged
    /// and skipped rather than propagated.
    pub fn from_provider_dir(&self, provider_dir: &Path) -> Result<ApiCatalog, String> {
        let mut apis = Vec::new();

        // Check for single spec at provider/openapi.json
        let single_spec = provider_dir.join("openapi.json");
        if single_spec.exists() {
            return self.from_spec_file(&single_spec);
        }

        // Look for sub-APIs in provider/api_name/openapi.json
        if let Ok(entries) = std::fs::read_dir(provider_dir) {
            for entry in entries.flatten() {
                if entry.path().is_dir() {
                    let api_name = entry.file_name().to_string_lossy().to_string();
                    let spec_path = entry.path().join("openapi.json");
                    if spec_path.exists() {
                        let content = match std::fs::read_to_string(&spec_path) {
                            Ok(c) => c,
                            Err(e) => {
                                eprintln!(
                                    "    Warning: Failed to read {}: {}",
                                    spec_path.display(),
                                    e
                                );
                                continue;
                            }
                        };

                        // Try to process the spec, skip if it fails
                        let processor = match process_spec(&content).or_else(|_| {
                            let wrapped: serde_json::Value = serde_json::from_str(&content)
                                .map_err(|e| format!("JSON parse error: {e}"))?;
                            if let Some(obj) = wrapped.as_object() {
                                for key in ["openapi.json", "openapi", "spec"] {
                                    if let Some(inner) = obj.get(key) {
                                        if let Ok(proc) = process_spec(&inner.to_string()) {
                                            return Ok(proc);
                                        }
                                    }
                                }
                            }
                            Err("Failed to parse spec".to_string())
                        }) {
                            Ok(p) => p,
                            Err(e) => {
                                eprintln!("    Warning: Failed to process {api_name}: {e}");
                                continue;
                            }
                        };

                        let endpoints = processor.endpoints();
                        let base_url = processor.base_url();

                        if !endpoints.is_empty() {
                            apis.push(ApiInfo {
                                name: api_name,
                                base_url,
                                endpoints,
                            });
                        }
                    }
                }
            }
        }

        // Sort APIs by name for consistent ordering
        apis.sort_by(|a, b| a.name.cmp(&b.name));

        Ok(ApiCatalog {
            provider: self.provider.clone(),
            apis,
        })
    }
}

impl ApiCatalog {
    /// Create a catalog builder for the given provider.
    #[must_use]
    pub fn builder(provider: &str) -> ApiCatalogBuilder {
        ApiCatalogBuilder::new(provider)
    }

    /// Get total number of endpoints across all APIs.
    #[must_use]
    pub fn total_endpoints(&self) -> usize {
        self.apis.iter().map(|api| api.endpoints.len()).sum()
    }

    /// Get all endpoints across all APIs.
    pub fn all_endpoints(&self) -> impl Iterator<Item = &EndpointInfo> {
        self.apis.iter().flat_map(|api| &api.endpoints)
    }

    /// Get a specific API by name.
    #[must_use]
    pub fn get_api(&self, name: &str) -> Option<&ApiInfo> {
        self.apis.iter().find(|api| api.name == name)
    }

    /// Get mutating endpoints (operations that require state tracking) for an API.
    #[must_use]
    pub fn mutating_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| ep.operation_type.requires_state_tracking())
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get read-only endpoints (operations that don't modify state) for an API.
    #[must_use]
    pub fn read_only_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| !ep.operation_type.requires_state_tracking())
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get create endpoints for an API.
    #[must_use]
    pub fn create_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| matches!(ep.operation_type, OperationType::Create))
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get read endpoints for an API.
    #[must_use]
    pub fn read_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| matches!(ep.operation_type, OperationType::Read))
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get update endpoints for an API.
    #[must_use]
    pub fn update_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| matches!(ep.operation_type, OperationType::Update))
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get delete endpoints for an API.
    #[must_use]
    pub fn delete_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| matches!(ep.operation_type, OperationType::Delete))
                    .collect()
            })
            .unwrap_or_default()
    }

    /// Get action endpoints for an API.
    #[must_use]
    pub fn action_endpoints(&self, api_name: &str) -> Vec<&EndpointInfo> {
        self.get_api(api_name)
            .map(|api| {
                api.endpoints
                    .iter()
                    .filter(|ep| matches!(ep.operation_type, OperationType::Action(_)))
                    .collect()
            })
            .unwrap_or_default()
    }
}

/// Discovers providers from an artefacts directory.
///
/// A provider is a directory that either:
/// - Contains an openapi.json file directly, OR
/// - Contains subdirectories each with an openapi.json file
///
/// # Errors
///
/// Returns an [`std::io::Error`] if `artefacts_dir` exists but cannot be
/// read (e.g. permission denied). A non-existent path is treated as an
/// empty result and is not an error.
pub fn discover_providers(artefacts_dir: &Path) -> Result<Vec<String>, std::io::Error> {
    let mut providers = Vec::new();

    if !artefacts_dir.exists() {
        return Ok(providers);
    }

    for entry in std::fs::read_dir(artefacts_dir)? {
        let entry = entry?;
        let path = entry.path();
        if path.is_dir() {
            let name = path
                .file_name()
                .and_then(|n| n.to_str())
                .map(std::string::ToString::to_string);
            if let Some(name) = name {
                // Check if this is a provider (has openapi.json or sub-APIs)
                if path.join("openapi.json").exists() || has_sub_apis(&path) {
                    providers.push(name);
                }
            }
        }
    }

    providers.sort();
    Ok(providers)
}

/// Check if a provider directory has sub-APIs.
#[must_use]
pub fn has_sub_apis(provider_dir: &Path) -> bool {
    if let Ok(entries) = std::fs::read_dir(provider_dir) {
        for entry in entries.flatten() {
            if entry.path().is_dir() && entry.path().join("openapi.json").exists() {
                return true;
            }
        }
    }
    false
}

/// Convert a string to `PascalCase`.
#[must_use]
pub fn to_pascal_case(s: &str) -> String {
    // First split by delimiters (., -, _, @)
    let parts: Vec<&str> = s.split(['.', '-', '_', '@']).collect();

    let mut result = String::new();

    for part in parts {
        // Within each part, split on case transitions and numbers
        let mut sub_parts = Vec::new();
        let mut current = String::new();
        let mut prev_was_digit = false;
        let mut prev_was_lower = false;

        for c in part.chars() {
            let is_digit = c.is_ascii_digit();
            let is_upper = c.is_ascii_uppercase();
            let is_lower = c.is_ascii_lowercase();

            // Split on digit <-> letter and lower -> upper transitions.
            let boundary = !current.is_empty()
                && ((is_digit && !prev_was_digit)
                    || (is_lower && prev_was_digit)
                    || (is_upper && prev_was_lower));
            if boundary {
                sub_parts.push(current.clone());
                current.clear();
            }

            current.push(c);
            prev_was_digit = is_digit;
            prev_was_lower = is_lower;
        }

        if !current.is_empty() {
            sub_parts.push(current);
        }

        // Capitalize each sub-part
        for sub in sub_parts {
            if let Some(first) = sub.chars().next() {
                result.push_str(&first.to_uppercase().to_string());
                if sub.len() > 1 {
                    result.push_str(&sub[1..]);
                }
            }
        }
    }

    result
}

/// Convert `PascalCase` or camelCase to `snake_case`.
///
/// Splits by delimiters first (preserving word boundaries), then converts
/// each part's camelCase to snake_case individually. This ensures that
/// delimiter-separated segments like "a2a.v1" produce "a2a_v1" rather
/// than merging into "a2_av1".
#[must_use]
pub fn to_snake_case(s: &str) -> String {
    s.split([
        '.', '-', '_', '@', ':', '<', '>', '[', ']', '(', ')', '\'', ',', '~', '/',
    ])
    .filter(|p| !p.is_empty())
    .map(|part| camel_to_snake(part))
    .collect::<Vec<_>>()
    .join("_")
}

/// Convert a single camelCase or PascalCase segment to snake_case.
///
/// Handles acronyms (e.g., "exportSBOM" -> "export_sbom", "OAuth" -> "oauth").
fn camel_to_snake(part: &str) -> String {
    let mut result = String::new();
    let chars: Vec<char> = part.chars().collect();
    let len = chars.len();
    let mut i = 0;

    while i < len {
        let c = chars[i];

        if c.is_ascii_uppercase() {
            // Check if this is start of an acronym (consecutive uppercase letters)
            let mut j = i + 1;
            while j < len && chars[j].is_ascii_uppercase() {
                j += 1;
            }

            // If we have multiple uppercase letters, it's an acronym
            if j - i > 1 {
                // Add underscore before acronym (if not at start)
                if i > 0 {
                    result.push('_');
                }

                // If there's a lowercase letter after the acronym, all but last are part of acronym
                if j < len && chars[j].is_ascii_lowercase() {
                    for k in i..j - 1 {
                        result.push(chars[k].to_ascii_lowercase());
                    }
                    result.push('_');
                    result.push(chars[j - 1].to_ascii_lowercase());
                    i = j;
                } else {
                    // Full acronym at end or followed by non-letter
                    for k in i..j {
                        result.push(chars[k].to_ascii_lowercase());
                    }
                    i = j;
                }
            } else {
                // Single uppercase letter
                if i > 0 {
                    result.push('_');
                }
                result.push(c.to_ascii_lowercase());
                i += 1;
            }
        } else {
            result.push(c);
            i += 1;
        }
    }

    result
}

/// Convert `snake_case` function name to sentence case for docs.
#[must_use]
pub fn to_sentence_case(s: &str) -> String {
    let mut result = s.replace('_', " ");
    if let Some(first) = result.chars().next() {
        result.replace_range(..1, &first.to_uppercase().to_string());
    }
    result
}

/// Sanitize a string identifier by replacing special characters with underscores.
///
/// Replaces: `- . @ : < > [ ] ( ) ' , ~ /` with `_`
///
/// # Examples
///
/// ```
/// use foundation_openapi::sanitize_identifier;
/// assert_eq!(sanitize_identifier("admin.channels.stop"), "admin_channels_stop");
/// assert_eq!(sanitize_identifier("foo-bar@baz"), "foo_bar_baz");
/// ```
#[must_use]
pub fn sanitize_identifier(s: &str) -> String {
    s.replace(
        [
            '-', '.', '@', ':', '<', '>', '[', ']', '(', ')', '\'', ',', '~', '/', ' ',
        ],
        "_",
    )
}

/// Convert an API path to a snake_case function name suffix.
///
/// Strips leading `/`, replaces `/` with `_`, removes `{` and `}`,
/// and sanitizes special characters.
///
/// # Examples
///
/// ```
/// use foundation_openapi::path_to_fn_suffix;
/// assert_eq!(path_to_fn_suffix("/projects/{project}/services"), "projects_project_services");
/// assert_eq!(path_to_fn_suffix("/tools/{id}:execute"), "tools_id_execute");
/// ```
#[must_use]
pub fn path_to_fn_suffix(path: &str) -> String {
    path.trim_matches('/')
        .replace('/', "_")
        .replace(['{', '}'], "")
        .replace(['-', '.', '@', ':', '<', '>', '[', ']'], "_")
        .replace(['(', ')', '\''], "")
        .replace([',', '~'], "_")
}

/// Convert an operation ID to a snake_case function name.
///
/// If operation_id is present, sanitizes and converts to snake_case.
/// Otherwise, generates from HTTP method + path.
///
/// # Examples
///
/// ```
/// use foundation_openapi::operation_id_to_fn_name;
/// assert_eq!(operation_id_to_fn_name(Some("CloudKmsProjectsGet"), "GET", "/projects"), "cloud_kms_projects_get");
/// assert_eq!(operation_id_to_fn_name(None, "GET", "/projects/{id}"), "get_projects_id");
/// ```
#[must_use]
pub fn operation_id_to_fn_name(operation_id: Option<&str>, method: &str, path: &str) -> String {
    if let Some(op_id) = operation_id {
        let sanitized = sanitize_identifier(op_id);
        to_snake_case(&sanitized)
    } else {
        let path_suffix = path_to_fn_suffix(path);
        format!("{}_{}", method.to_lowercase(), path_suffix)
    }
}

/// Escape Rust keywords by appending `_rs`.
#[must_use]
pub fn escape_rust_keyword(name: &str) -> String {
    match name {
        "as" | "break" | "const" | "continue" | "crate" | "else" | "enum" | "extern" | "false"
        | "fn" | "for" | "if" | "impl" | "in" | "let" | "loop" | "match" | "mod" | "move"
        | "mut" | "pub" | "ref" | "return" | "self" | "Self" | "static" | "struct" | "super"
        | "trait" | "true" | "type" | "unsafe" | "use" | "where" | "while" | "async" | "await"
        | "dyn" | "override" => format!("{}_rs", name),
        _ => name.to_string(),
    }
}

/// Escape Rust keywords by appending underscore (alternative style).
#[must_use]
pub fn escape_rust_keyword_with_underscore(name: &str) -> String {
    match name {
        // Strict keywords
        "as" | "break" | "const" | "continue" | "crate" | "else" | "enum" | "extern"
        | "false" | "fn" | "for" | "if" | "impl" | "in" | "let" | "loop" | "match"
        | "mod" | "move" | "mut" | "pub" | "ref" | "return" | "self" | "Self"
        | "static" | "struct" | "super" | "trait" | "true" | "type" | "unsafe"
        | "use" | "where" | "while" | "async" | "await" | "dyn"
        // Reserved/weak keywords
        | "abstract" | "become" | "box" | "do" | "final" | "macro" | "override" | "priv"
        | "typeof" | "unsized" | "virtual" | "yield" => format!("{name}_"),
        _ => name.to_string(),
    }
}

/// Sanitize a field name: convert to snake_case and handle keywords/numeric prefixes.
#[must_use]
pub fn sanitize_field_name(name: &str) -> String {
    let snake = to_snake_case(&sanitize_identifier(name));
    let escaped = escape_rust_keyword_with_underscore(&snake);

    // Prepend "field_" if starts with digit
    if escaped.chars().next().is_some_and(|c| c.is_ascii_digit()) {
        format!("field_{}", escaped)
    } else {
        escaped
    }
}

/// Convert a name to PascalCase, handling special characters and acronyms.
#[must_use]
pub fn to_pascal_case_from_any(name: &str) -> String {
    let snake = to_snake_case(&sanitize_identifier(name));
    snake
        .split('_')
        .filter(|s| !s.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => {
                    let upper: String = first.to_uppercase().collect();
                    upper + chars.as_str()
                }
                None => String::new(),
            }
        })
        .collect::<String>()
        .if_empty(|| "Unknown".to_string())
}

/// Sanitize a documentation comment for Rust doc strings.
///
/// This function:
/// 1. Removes existing backticks (to avoid unbalanced backticks in rustdoc)
/// 2. Escapes single quotes for rustdoc (e.g., `timestamp('2020-01-01')` → `timestamp(''2020-01-01'')`)
/// 3. Wraps path-like patterns in backticks (e.g., `projects/{id}/services`)
/// 4. Wraps type names (String, i32, Vec<T>, Option<T>) in backticks
/// 5. Wraps enum-like values (TRUE, FALSE, OK, ERROR, PENDING) in backticks
/// 6. Wraps camelCase identifiers in backticks
/// 7. Wraps boolean literals (true, false, null) in backticks
/// 8. Converts HTML `<code>` tags to plain text
/// 9. Removes other HTML tags
/// 10. Converts bare URLs to angle-bracket links
/// 11. Escapes stray angle brackets with &lt; and &gt;
/// 12. Optionally truncates to first line for field-level comments
///
/// # Arguments
///
/// * `text` - The documentation text to sanitize
/// * `full` - If `false`, only the first line is returned (for field comments)
///
/// # Examples
///
/// ```
/// use foundation_openapi::sanitize_doc_comment;
/// let doc = sanitize_doc_comment("The project ID. See https://cloud.google.com", false);
/// assert!(doc.contains("<https://cloud.google.com>"));
/// ```
#[must_use]
pub fn sanitize_doc_comment(text: &str, full: bool) -> String {
    use regex::Regex;

    if text.is_empty() {
        return String::new();
    }

    let mut result = text.to_string();

    // 1. Strip ALL backticks to avoid issues with rustdoc parsing
    result = result.replace('`', "");

    // 2. Escape single quotes to prevent them being interpreted as character literals
    // This is necessary for examples like: timestamp('2020-10-01T00:00:00Z')
    result = result.replace('\'', "''");

    // 3. Wrap path-like patterns in backticks
    let path_re = Regex::new(r"([a-z]+/[\w{/}-]+)").unwrap();
    result = path_re.replace_all(&result, "`$1`").to_string();

    // 4. Wrap type-like patterns
    let type_re = Regex::new(r"\b(String|i32|i64|bool|f64|Vec<[^>]+>|Option<[^>]+>)\b").unwrap();
    result = type_re.replace_all(&result, "`$1`").to_string();

    // 5. Wrap enum-like values
    let enum_re = Regex::new(r"\b(TRUE|FALSE|OK|ERROR|PENDING|ACTIVE|INACTIVE)\b").unwrap();
    result = enum_re.replace_all(&result, "`$1`").to_string();

    // 6. Wrap code-like identifiers (camelCase or single words that look like code)
    // Matches things like `returnPartialSuccess`, `maxResults`, etc.
    let code_re = Regex::new(r"\b([a-z][a-zA-Z0-9]+(?:[A-Z][a-zA-Z0-9]+)+)\b").unwrap();
    result = code_re.replace_all(&result, "`$1`").to_string();

    // 7. Wrap boolean literals
    let bool_re = Regex::new(r"\b(true|false|null)\b").unwrap();
    result = bool_re.replace_all(&result, "`$1`").to_string();

    // 8. Convert HTML <code> tags to plain text (no backticks)
    let code_tag_re = Regex::new(r"<code>([^<]+)</code>").unwrap();
    result = code_tag_re.replace_all(&result, "$1").to_string();

    // 9. Remove other HTML tags but keep content
    let html_tag_re = Regex::new(r"</?[^>]+>").unwrap();
    result = html_tag_re.replace_all(&result, "").to_string();

    // 10. Convert bare URLs to angle-bracket links
    let url_re = Regex::new(r"(https?://[^\s<>\[\]()]+)").unwrap();
    result = url_re.replace_all(&result, "<$1>").to_string();

    // 11. Escape stray angle brackets not in backticks or URLs
    // Note: We already escaped single quotes, so we just need to handle angle brackets
    let mut escaped = String::new();
    let mut in_backticks = false;
    let mut in_url = false;
    let mut chars = result.chars().peekable();

    while let Some(c) = chars.next() {
        if c == '`' {
            in_backticks = !in_backticks;
            escaped.push(c);
        } else if c == '<' && !in_backticks {
            // Check if this looks like a URL start
            let rest: String = chars.clone().collect();
            if rest.starts_with("http://") || rest.starts_with("https://") {
                in_url = true;
                escaped.push(c);
            } else {
                escaped.push_str("&lt;");
            }
        } else if c == '>' && !in_backticks && in_url {
            in_url = false;
            escaped.push(c);
        } else if c == '>' && !in_backticks {
            escaped.push_str("&gt;");
        } else {
            escaped.push(c);
        }
    }
    result = escaped;

    // 12. For field comments, use only first line
    if !full {
        result = result.lines().next().unwrap_or(&result).to_string();
    } else {
        // For full comments, ensure each line is properly formatted
        // Join lines with "/// " prefix for multi-line descriptions
        result = result
            .lines()
            .map(|line| line.trim())
            .collect::<Vec<_>>()
            .join(" ");
    }

    result
}

// Helper trait for if_empty
trait IfEmpty {
    fn if_empty<F>(&self, f: F) -> String
    where
        F: FnOnce() -> String;
}

impl IfEmpty for String {
    fn if_empty<F>(&self, f: F) -> String
    where
        F: FnOnce() -> String,
    {
        if self.is_empty() {
            f()
        } else {
            self.clone()
        }
    }
}

